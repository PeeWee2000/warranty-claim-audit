# ---------------------------------------------------------------------------
# Convenience look-ups
# ---------------------------------------------------------------------------

DTC_BY_CODE: dict[str, DTCEntry] = {d["code"]: d for d in DTC_DATABASE}

DTC_BY_CATEGORY: dict[str, list[DTCEntry]] = {}
for _d in DTC_DATABASE:
    DTC_BY_CATEGORY.setdefault(_d["category"], []).append(_d)

DTC_BY_SUBCATEGORY: dict[str, list[DTCEntry]] = {}
for _d in DTC_DATABASE:
    DTC_BY_SUBCATEGORY.setdefault(_d["subcategory"], []).append(_d)

DTC_BY_SEVERITY: dict[str, list[DTCEntry]] = {}
for _d in DTC_DATABASE:
    DTC_BY_SEVERITY.setdefault(_d["severity"], []).append(_d)


def get_codes_for_category(category: str) -> list[DTCEntry]:
    """Return all DTCs matching the given category."""
    return DTC_BY_CATEGORY.get(category, [])


def get_codes_for_subcategory(subcategory: str) -> list[DTCEntry]:
    """Return all DTCs matching the given subcategory."""
    return DTC_BY_SUBCATEGORY.get(subcategory, [])


def get_codes_for_severity(severity: str) -> list[DTCEntry]:
    """Return all DTCs matching the given severity level."""
    return DTC_BY_SEVERITY.get(severity, [])


def get_code(code: str) -> DTCEntry | None:
    """Look up a single DTC by its code string (e.g. 'P0420')."""
    return DTC_BY_CODE.get(code)


